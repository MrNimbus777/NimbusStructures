package net.nimbus.structures;

public class Point3 {
    int x;
    int y;
    int z;
    public Point3(int x, int y, int z){
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getZ() {
        return z;
    }

    public int getY() {
        return y;
    }
}
